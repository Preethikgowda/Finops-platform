class TestGenerateApprovalWorkflow:
    def test_contains_approve_button(self):
        workflow = generate_approval_workflow(
            pr_url="https://github.com/org/repo/pull/1",
            changes_summary="2 EC2 instances to downsize",
        )
        # Find the actions block which contains the buttons
        actions_block = next(b for b in workflow["blocks"] if b.get("type") == "actions")
        action_ids = [e["action_id"] for e in actions_block["elements"]]
        assert "finops_approve_apply" in action_ids

    def test_contains_reject_button(self):
        workflow = generate_approval_workflow(
            pr_url="https://github.com/org/repo/pull/1",
            changes_summary="Test",
        )
        actions_block = next(b for b in workflow["blocks"] if b.get("type") == "actions")
        action_ids = [e["action_id"] for e in actions_block["elements"]]
        assert "finops_reject" in action_ids

    def test_pr_url_in_message(self):
        pr_url = "https://github.com/org/repo/pull/42"
        workflow = generate_approval_workflow(pr_url=pr_url, changes_summary="x")
        assert pr_url in json.dumps(workflow)

    def test_fallback_text_present(self):
        workflow = generate_approval_workflow(
            pr_url="https://github.com/org/repo/pull/1",
            changes_summary="",
        )
        assert "text" in workflow


