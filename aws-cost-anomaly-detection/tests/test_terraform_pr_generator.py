class TestEstimateChangeRisk:
    def test_same_family_ec2_is_low_risk(self):
        risk = estimate_change_risk("ec2", "t3.large", "t3.medium")
        assert risk["risk_level"] == "low"

    def test_cross_family_m5_to_t3_is_medium_risk(self):
        risk = estimate_change_risk("ec2", "m5.xlarge", "t3.large")
        assert risk["risk_level"] == "medium"

    def test_rds_same_family_is_low_risk(self):
        risk = estimate_change_risk("rds", "db.m5.xlarge", "db.m5.large")
        assert risk["risk_level"] == "low"

    def test_lambda_is_always_low_risk(self):
        risk = estimate_change_risk("lambda", "1024", "512")
        assert risk["risk_level"] == "low"
        assert risk["estimated_downtime"] == "0 seconds"

    def test_missing_config_returns_high_risk(self):
        risk = estimate_change_risk("ec2", "", "t3.medium")
        assert risk["risk_level"] == "high"

    def test_unknown_resource_type_returns_high_risk(self):
        risk = estimate_change_risk("elasticache", "cache.r5.large", "cache.t3.medium")
        assert risk["risk_level"] == "high"

    def test_reasoning_is_non_empty(self):
        risk = estimate_change_risk("ec2", "m5.xlarge", "t3.large")
        assert risk["reasoning"]
        assert risk["estimated_downtime"]


