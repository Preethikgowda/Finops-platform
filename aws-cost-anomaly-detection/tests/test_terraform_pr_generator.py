class TestGenerateEc2DownsizingTerraform:
    def test_output_contains_resource_block(self, ec2_recommendation):
        tf = generate_ec2_downsizing_terraform([ec2_recommendation])
        assert 'resource "aws_instance"' in tf

    def test_output_contains_recommended_type(self, ec2_recommendation):
        tf = generate_ec2_downsizing_terraform([ec2_recommendation])
        assert 't3.large' in tf

    def test_output_contains_previous_type_comment(self, ec2_recommendation):
        tf = generate_ec2_downsizing_terraform([ec2_recommendation])
        assert "m5.xlarge" in tf

    def test_output_contains_savings_comment(self, ec2_recommendation):
        tf = generate_ec2_downsizing_terraform([ec2_recommendation])
        assert "$80" in tf or "80.00" in tf

    def test_output_contains_date_header(self, ec2_recommendation):
        tf = generate_ec2_downsizing_terraform([ec2_recommendation])
        assert "Auto-generated by FinOps" in tf

    def test_empty_recommendations_returns_header_only(self):
        tf = generate_ec2_downsizing_terraform([])
        assert "Auto-generated" in tf
        assert 'resource "aws_instance"' not in tf

    def test_multiple_recommendations(self, ec2_recommendation):
        recs = [
            ec2_recommendation,
            {**ec2_recommendation, "instance_id": "i-second", "name": "another-svc"},
        ]
        tf = generate_ec2_downsizing_terraform(recs)
        assert tf.count('resource "aws_instance"') == 2

    def test_lifecycle_block_present(self, ec2_recommendation):
        tf = generate_ec2_downsizing_terraform([ec2_recommendation])
        assert "lifecycle" in tf
        assert "ignore_changes" in tf


