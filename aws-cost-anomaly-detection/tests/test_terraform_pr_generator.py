class TestCommitAndCreateGithubPr:
    def test_missing_github_token_raises(self):
        with patch.dict(os.environ, {"GITHUB_TOKEN": "", "GITHUB_REPO": "org/repo"}):
            with pytest.raises(EnvironmentError, match="GITHUB_TOKEN"):
                terraform_pr_generator.commit_and_create_github_pr(
                    tf_changes={}, title="Test", description="body"
                )

    def test_missing_github_repo_raises(self):
        with patch.dict(os.environ, {"GITHUB_TOKEN": "ghp_test", "GITHUB_REPO": ""}):
            with pytest.raises(EnvironmentError, match="GITHUB_REPO"):
                terraform_pr_generator.commit_and_create_github_pr(
                    tf_changes={}, title="Test", description="body"
                )

    def test_pr_created_successfully(self):
        mock_gh = MagicMock()
        mock_repo = MagicMock()
        mock_branch_ref = MagicMock()
        mock_branch_ref.commit.sha = "abc123"
        mock_repo.get_branch.return_value = mock_branch_ref

        mock_pr = MagicMock()
        mock_pr.html_url = "https://github.com/org/repo/pull/99"
        mock_repo.create_pull.return_value = mock_pr
        mock_repo.get_pulls.return_value = iter([])

        # Simulate file doesn't exist yet
        from github.GithubException import UnknownObjectException
        mock_repo.get_contents.side_effect = UnknownObjectException(404, {}, {})

        mock_gh.get_repo.return_value = mock_repo

        with patch.dict(
            os.environ,
            {"GITHUB_TOKEN": "ghp_test", "GITHUB_REPO": "org/repo", "GITHUB_BRANCH_MAIN": "main"},
        ), patch("terraform_pr_generator.Github", return_value=mock_gh):
            pr_url = terraform_pr_generator.commit_and_create_github_pr(
                tf_changes={"terraform/test.tf": "# content"},
                title="Cost Optimization: Test",
                description="body text",
            )

        assert pr_url == "https://github.com/org/repo/pull/99"
        mock_repo.create_git_ref.assert_called_once()
        mock_repo.create_file.assert_called_once()
        mock_repo.create_pull.assert_called_once()

    def test_existing_pr_skips_creation(self):
        mock_gh = MagicMock()
        mock_repo = MagicMock()

        existing_pr = MagicMock()
        existing_pr.title = "Cost Optimization: Test"
        existing_pr.html_url = "https://github.com/org/repo/pull/5"
        mock_repo.get_pulls.return_value = iter([existing_pr])
        mock_gh.get_repo.return_value = mock_repo

        with patch.dict(
            os.environ,
            {"GITHUB_TOKEN": "ghp_test", "GITHUB_REPO": "org/repo"},
        ), patch("terraform_pr_generator.Github", return_value=mock_gh):
            pr_url = terraform_pr_generator.commit_and_create_github_pr(
                tf_changes={},
                title="Cost Optimization: Test",
                description="body",
            )

        assert pr_url == "https://github.com/org/repo/pull/5"
        mock_repo.create_git_ref.assert_not_called()

    def test_pygithub_not_available_raises_import_error(self):
        with patch.object(terraform_pr_generator, "_GITHUB_AVAILABLE", False):
            with pytest.raises(ImportError, match="PyGithub"):
                terraform_pr_generator.commit_and_create_github_pr(
                    tf_changes={}, title="T", description="D"
                )

    def test_branch_name_matches_expected_pattern(self):
        mock_gh = MagicMock()
        mock_repo = MagicMock()
        mock_repo.get_branch.return_value.commit.sha = "sha1"
        mock_repo.get_pulls.return_value = iter([])

        from github.GithubException import UnknownObjectException
        mock_repo.get_contents.side_effect = UnknownObjectException(404, {}, {})

        mock_pr = MagicMock()
        mock_pr.html_url = "https://github.com/org/repo/pull/10"
        mock_repo.create_pull.return_value = mock_pr
        mock_gh.get_repo.return_value = mock_repo

        created_refs: list[str] = []
        mock_repo.create_git_ref.side_effect = lambda ref, sha: created_refs.append(ref)

        with patch.dict(
            os.environ,
            {"GITHUB_TOKEN": "ghp_test", "GITHUB_REPO": "org/repo"},
        ), patch("terraform_pr_generator.Github", return_value=mock_gh):
            terraform_pr_generator.commit_and_create_github_pr(
                tf_changes={"terraform/t.tf": "# tf"},
                title="PR",
                description="desc",
            )

        assert len(created_refs) == 1
        assert created_refs[0].startswith("refs/heads/auto/cost-optimization-")
